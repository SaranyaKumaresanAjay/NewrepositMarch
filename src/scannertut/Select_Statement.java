package scannertut;

public class Select_Statement {
	public static void main(String[] args) {
	int a=10;
	
	if (a<=10) {
		System.out.println(1);
	}
	
	if(a>=5) {
	System.out.println(2);
	}	
	
	if(a==10) {
	System.out.println(3);
	}	

	if(a<=50 && a>=5) {
	System.out.println(4);
}

	if(a==10 || a>=50) {
	System.out.println(5);
}
}
}