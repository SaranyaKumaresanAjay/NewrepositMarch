public class Iteration_No {
public static void main(String[] args) {
	int a=25;
	System.out.println(a++);
	System.out.println(a++);
	System.out.println(a++);
	System.out.println(++a);
	System.out.println(--a );
	System.out.print(++a+"," );
	System.out.println(a++);
	System.out.println(a+2);
	System.out.println(--a);
	System.out.println("The Iteration is completed");
	
}}
